/**
 * Represents the dietary restriction of a recipe.
 */
export enum DietaryRestriction {
  /**
   * Vegan.
   */
  Vegan,

  /**
   * Vegetarian.
   */
  Vegetarian,

  /**
   * Pescatarian.
   */
  Pescatarian,

  /**
   * Gluten-free.
   */
  GlutenFree,

  /**
   * Dairy-free.
   */
  DairyFree,

  /**
   * Nut-free.
   */
  NutFree,

  /**
   * Kosher.
   */
  Kosher,

  /**
   * Halal.
   */
  Halal,
}
